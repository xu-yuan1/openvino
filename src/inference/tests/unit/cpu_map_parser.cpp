int Hex2Int(char c) {
    return (c >= '0' && c <= '9')
               ? (c) - '0'
               : (c >= 'A' && c <= 'F') ? (c) - 'A' + 10 : (c >= 'a' && c <= 'f') ? (c) - 'a' + 10 : 0;
}

void Hex2Bin(const char* hex, std::size_t sz, char* out) {
    for (int i = 0; i < sz; i += 2) {
        out[i / 2] = (Hex2Int(hex[i]) << 4) | Hex2Int(hex[i + 1]);
    }
}

